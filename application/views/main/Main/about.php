        <h1>Technology Overview</h1>
        <br/>
        <div class="container">
            <h3>Blockchain Technology</h3>
            <br/>
            <p>GAMBOL aims to be a decentralised online gaming platform. For this to happen, Gambol <br/>
            will run on blockchain technologies. GAMBOL’s architecture will be built on top of <br/>
            Ethereum frameworks.</p>
            <br/>
            <p>By using a blockchain, the platform assures transparency to players, hosts and other <br/>
            stakeholders. On the GAMBOL platform two types of information will go onto the <br/>
            blockchain. These are:</p>
            <br/>
            <ol>
                <li>The results of each gameplay round</li>
                <li>The winning conditions and transactions of each round.</li>
            </ol>
        </div>
        <div class="container">
            <h3>Smart Contract</h3>
            <br/>
            <p>Smart contracts are agreements written in code that self-execute when pre-determined<br/> 
                conditions are met [8]. GAMBOL smart contracts will ensure that games play out according<br/> 
                to pre-agreed rules. They sustain fairness, transparency and trust among those involved. <br/>
                Depending on the game, the winning algorithm will be stated in the smart contract, <br/>
                something any of the participant can check out by accessing the software source code.<br/>
                Once a game session ends, the smart Contract will fetch the results and guide their <br/>
                forwarding onto the Blockchain through connected servers.</p>
            <br/>
            <p></p>
        </div>
        <div class="container">
            <h3>ERC20</h3>
            <br/>
            <p>To make smart contracts viable both on GAMBOL and on Ethereum blockchain, the BOL <br/>
                token that will be the means of value transmission is designed to meet ERC-20 standards [9].<br/>
                That means it can be used within the ecosystem of the Ethereum blockchain. It also means it <br/>
                qualifies to be listed on major cryptocurrency exchanges and can be held in commonly used <br/>
                wallets. This makes the trading of the token easier.</p>
        </div>
        <div class="container">
            <h3>User ﬂow </h3>
            <br/>
            <p>First, users login and authenticate through the GAMBOL servers. This produces an access <br/>
            token to be used in multiple games. They then have the choice to either host their own games <br/>
            or join sessions created by others. </p>
            <br/>
            <p>All games added and veriﬁed on GAMBOL will have direct interaction to the blockchain <br/>
            either through provided a software development kit (SDK) or an application programming <br/>
            interface (API).</p>
            <br/>
            <p>At the start of every game round, a smart contract is created to guide it and make sure <br/>
            promises made are honoured.</p>
        </div>
        <img class="img-fluid" src="<?= base_url() ?>/images/smart_contract.png">
        <div class="container">
            <h3>PvP Games</h3>
            <br/>
            <p>GAMBOL will run games and players will connect to the network through a browser. They <br/>
            can join games others are hosting or opt to host themselves if they meet the conditions. </p>
            <br/>
            <p>GAMBOL’s primary role is to ensure the results of the game are always updated according to <br/>
            pre-agreed rules and players get the promised rewards when they win. </p>
        </div>
        <div class="container">
            <h3>P2P Hosting</h3>
            <br/>
            <p>GAMBOL will provide an infrastructure to host peer-to-peer games. These games are hosted<br/>
            on each player’s devices instead of the GAMBOL server. The P2P hosting provides a chance</br>
            for third party developers to build games without touching the GAMBOL main </br/>
            infrastructures. However the SDKs and APIs will allow the games to interact with the <br/>
            GAMBOL platform and have their sessions recorded on the blockchain.</p>
        </div>
        <div class="container">
            <h3>Security </h3>
            <br/>
            <p>The GAMBOL follows the latest security standards available, namely the CCSS standards <br/>
            [10]. This will ensure all our private keys are stored in the most recommended manner. </p>
            <br/>
            <p>Smart contracts deployed by GAMBOL and 3rd party developers will go through unit testing <br/>
            before receiving live access to the gambol infrastructure. This will provide a safe <br/>
            environment for all stakeholders.</p>
        </div>
        <div class="container">
            <h3>3rd Party Development  </h3>
            <br/>
            <p>GAMBOL will provide an SDK and API access for all third party developers. However, only <br/>
            limited information will be shared towards the external parties. A third party developer will <br/>
            have to go through an OAuth2— the industry-standard protocol for authorization—process in <br/>
            order to obtain an access token to the system.</p>
        </div>
        <div class="container">
            <h3>The GAMBOL wallet</h3>
            <br/>
            <p>Players will not use fiat money (such USD) to enter bets on the GAMBOL platform. Instead <br/>
            they will have to use the BOL token. That is because smart contract applications do not <br/>
            support fiat currencies. Also creating and using our own token is necessary to ensure a truly <br/>
            secure and transparent betting platform.</p>
            <p>As a token fully compliant with the ERC20 standard, the BOL token is compatible with Ether <br/>
            wallets such as MyEtherWallet, and it is also tradable on major crypto exchanges. To provide <br/>
            more options for players we plan to release an open-source GAMBOL Wallet app for iOS, <br/>
            Android, PC and Mac. </p>
            <p>In addition to strong fund protection and other exclusive features, the GAMBOL Wallet will <br/>
            allow players to easily obtain BOL tokens, as the app will be interlinked with third-party <br/>
            exchanges through APIs.</p>
