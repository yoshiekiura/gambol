        <h3>Cryptocurrency markets</h3>
        <br/>
        <p>In the traditional payment methods, centralized authorities such as banks and other financial
        institutions maintain the ledger on which transactions get recorded. When Satoshi Nakamoto
        created and launched Bitcoin, the process of maintaining a transaction ledger was
        revolutionized.</p> 
        <br/>
        <p>Bitcoin introduced and proved a concept of a community, through consensus on a peer-to-
        peer network of computers, maintaining a ledger instead of a central authority. The shared
        public ledger is more secure and reliable than one that a central authority oversees. </p>
        <br/>
        <p>The shared public ledger is what is known as blockchain, and is useful in more ways than
        recording financial transactions. In particular, it can serve as the platform on which 
        developers can host and launch applications such as those related to online gaming. The
        blockchain removes the central authority from the picture and makes decentralized platforms 
        on the Internet viable.</p>
        <br/>
        <p>Like online gaming, the cryptocurrency market is growing at a considerably high rate. In a
        span of nine years, the amount of investment held in cryptocurrencies has exceeded US$400
        billion—total market capitalization.</p>
        <br/>
        <p>In total there are over 1000 active cryptocurrencies, and a similar number or more number of
        digital tokens on various blockchains. Cryptocurrencies that launched after the success of 
        Bitcoin are called altcoins—shortened from alternative coins. Some of the altcoins with the
        highest market capitalizations include Ethereum, Ripple and Litecoin.</p>
        <br/>
        <p>According CoinMarketCap, a site that tracks cryptocurrency markets, daily trade volumes 
        exceed US$15 billion, with 41.8% of that being of Bitcoin [4].</p> 
        <br/>
        <p>What drives the adoption of cryptocurrencies is their decentralised nature. It is important that
        they are not subject to the whims of a few trusted policy makers, as with the traditional
        currencies. Their market value is truly determined by supply and demand.</p>
