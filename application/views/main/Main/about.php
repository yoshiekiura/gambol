            <br/>
            <p>First level: 15%</p>
            <br/>
            <p>Second level: 5%</p>
            <br/>
            <p>Third level: 5%</p>
            <br/>
            <p>For example, if player A wins 1000 BOL tokens during a game session, when they exit, they <br/>
            are charged 10 BOL token (1% commission rate). A first level referral will earn 1.5 BOL <br/>
            token from that amount. Second and third level referral users will earn 0.5 BOL token each. </p>
        </div>
        <div class="container">
            <h3>3rd Party Game Developer</h3>
