        <h3>Cryptocurrency markets</h3>
        <br/>
        <p>In the traditional payment methods, centralized authorities such as banks and other financial<br/> 
        institutions maintain the ledger on which transactions get recorded. When Satoshi Nakamoto <br/>
        created and launched Bitcoin, the process of maintaining a transaction ledger was <br/>
        revolutionized.</p> 
        <br/>
        <p>Bitcoin introduced and proved a concept of a community, through consensus on a peer-to-<br/>
        peer network of computers, maintaining a ledger instead of a central authority. The shared<br/>
        public ledger is more secure and reliable than one that a central authority oversees. </p>
        <br/>
        <p>The shared public ledger is what is known as blockchain, and is useful in more ways than<br/>
        recording financial transactions. In particular, it can serve as the platform on which <br/>
        developers can host and launch applications such as those related to online gaming. The <br/>
        blockchain removes the central authority from the picture and makes decentralized platforms <br/>
        on the Internet viable.</p>
        <br/>
        <p>Like online gaming, the cryptocurrency market is growing at a considerably high rate. In a<br/>
        span of nine years, the amount of investment held in cryptocurrencies has exceeded US$400<br/>
        billion—total market capitalization.</p>
        <br/>
        <p>In total there are over 1000 active cryptocurrencies, and a similar number or more number of <br/>
        digital tokens on various blockchains. Cryptocurrencies that launched after the success of<br/> 
        Bitcoin are called altcoins—shortened from alternative coins. Some of the altcoins with the <br/>
        highest market capitalizations include Ethereum, Ripple and Litecoin.</p>
        <br/>
        <p>According CoinMarketCap, a site that tracks cryptocurrency markets, daily trade volumes <br/>
        exceed US$15 billion, with 41.8% of that being of Bitcoin [4].</p> 
        <br/>
        <p>What drives the adoption of cryptocurrencies is their decentralised nature. It is important that <br/>
