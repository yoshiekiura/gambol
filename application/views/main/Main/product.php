<section id="gambol_solution" class="star-section">
    <div class="container">
        <h2>The GAMBOL solution  </h2>
        <br/>
        <div class="container">
            <p>The developers behind GAMBOL have put time and effort to identify weaknesses in the current online gaming architecture. 
            They have invested even more into designing viable solutions that leverage on the opportunities blockchain and smart contracts offer.</p>
            <br/>
            <p>The utmost goal is to empower and protect players, hosts and developers equally. Notwithstanding the class of stakeholder, the platform 
            will protect good actors from bad actors while everyone gets a smooth and enhanced experience.</p>
            <br/>
            <p>The specific solutions take the following shapes:</p>
            <br/>
            <p><strong>Fairness</strong> - All games are designed with smart contracts, which self-execute without intervention. The code of every game 
            on our platform can be veriﬁed on GitHub. GAMBOL will not host any games but will facilitates hosting and playing of games. The main objective 
            is to level the ground for everyone and makes it impossible for anyone to manipulate results.</p>
            <br/>
            <p><strong>Transparency</strong> - The games RNG algorithm will be the implementation of provably fair algorithm [1] on the blockchain. That 
            means during the generation of randomness, players receive key data that they can use to proof that the host doesn’t game the system. 
            In addition, much of the process is guided with smart contracts thus taking the potential of manipulation off the hands of the host and 
            maintaining a 100% transparency for everyone.</p>
            <br/>
            <p><strong>Player centric</strong> - GAMBOL is a total decentralised platform that facilitates player versus player (PvP) interactions. 
            It is a platform that is built with a priority for peer-to-peer interaction. This makes each player the centre of every action that they are 
            involved in. And this forces game developers to design their games with the view of the player at the top of the list of considerations.</p>
            <br/>
            <p>In particular, the blockchain and smart contracts makes it a reality that player will have true ownership of digital game assets such as 
            tokens. Players can move around and even outside the platform with their asset collections. This is because the ownership is not recorded 
            on a centralized server database in control of a third party but on a shared public ledger.</p> 
            <br/>
            <p><strong>Security</strong> - GAMBOL using blockchain and smart contracts offers security for user data and lowers risk that come with 
            payment methods. On centralized platforms, players are forced to use traditional payment methods, which collect personally identifying data. 
            The databases on which this data is kept become attractive to hackers. </p>
            <br/>
            <p>To make it unnecessary for stakeholders to give personally identifying information, all payments within GAMBOL platform will be through 
            cryptocurrencies. Users are welcome to use Bitcoin, Ethereum and a few other coins that will be added later. </p>
            <br/>
            <p>But for in-game value transactions, GAMBOL offers the Bol token (BOL) that is easy to integrate with games on the platform. The BOL token 
            meets the ERC20 standards, meaning its transactions get recorded on the Ethereum blockchain and it can be used through existing wallets. </p>
            <br/>
            <p><strong>Less Costly</strong> - GAMBOL will collect low or insignificant transaction fees. This is made possible by the fact that the platform 
            is on top of a public blockchain, which is not a central authority with a lot of overheads. </p>
            <br/>
            <p>Part of the transaction fee will go into settling gas payment on the Ethereum blockchain and the rest to maintain the infrastructure. </p>
            <br/>
            <p><strong>Open Source</strong> - The GAMBOL without stringent conditions will provide an open API for game developer to create games and 
            integrate with the platform. And the source code for all games will be available on GitHub.</p>
        </div>
        
