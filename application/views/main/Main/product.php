<section id="main_concept" class="color-dark-section">
    <div class="container">
        <h2>GAMBOL main concept   </h2>
        <br/>
        <div class="container">
            <p>GAMBOL is a decentralised platform for games of skill. It integrates innovative solutions that blockchain technology provides to make 
            online gaming fair and transparent, leaving the outcome to player’s skills. </p>
            <br/>
            <p>The platform solves trust issues extending to itself. Every game on GAMBOL platform integrates with smart contract whose algorithms can 
            be reviewed by anyone on an open public ledger as well as on the code control platform GitHub. </p>
            <br/>
            <p>With immense help from blockchain, GAMBOL system will be absolutely transparent. The outcome of each individual round of game is random 
            and will be determined by multi factor model of random number generation (RNG) algorithm, where the ﬁnal result is determined based on 
            several input parameters. The players are able to proof after the fact the random generation wasn’t manipulated. </p>
            <br/>
            <p>On GAMBOL, anyone with BOL tokens can become a host as much as a player. To be a host however, one needs to hold a certain minimum amount 
            of the BOL tokens in their wallet to support the running of the game. The host creates a game room, decide on the bet volumes and invite 
            players to participate. </p>
            <br/>
            <p>Once the host decides on the details of a game, the system generates a smart contract with the rules that will guide it. The smart contract 
                enters into an autopilot mode once the game begins and all the processes are out of the host’s control.</p>
            <br/>
            <p>The pay-outs system is automatic as well and based on the smart contract. In every individual game, the blockchain receives, from designated 
            servers, and records final tallies, which become immutable—the record cannot be changed once it is made. The system then, as stipulated in 
            the smart contract, deducts from or makes pay outs to players. </p>
            <br/>
            <p>The BOL token, is the main in-game currency. The use of BOL token will be for betting in game, reward program, bonus and also pay out for 
            aﬃliate program. By using BOL token, it allows every player to bet with minimal transaction and lower down the transaction fee. Moreover, 
            BOL token can minimise the system time spent on game processing, transferring of funds and ensures high levels of liquidity. </p>
            <br/>
            <p>GAMBOL players are allow to exchange their tokens (BOL) at any given time to their preferred cryptocurrency and withdraw funds. GAMBOL will 
            register the BOL token on major cryptocurrency exchanges. </p>
        </div>
    </div>
