<section class="star-section">
	<div class="container">
        <h3>Game Commission</h3>
        <br/>
        <p>GAMBOL will take a commission only when player exits a game room, no matter how many rounds of game happened. Also they onetime commission applies only on the winning player. </p>
        <br/>
        <p>For example, player A enters the room with 10,000 BOL token as a host, and player B enters the room with 1000 BOL token as just a player. After 10 round of games, player B win 500 BOL token overall, and they exit the room. GAMBOL will take commission only from player B’s 500 BOL tokens winning. </p>
        <br/>
        <p>If player B loses 500 BOL tokens overall at the point of exiting the room, they will not be charged any commission.</p>
        <br/>
        <p>The game rooms will have refresh time intervals that the hosts decide to have. When the refresh timing reach, every player calculates their commission rate before the game continues.</p>
